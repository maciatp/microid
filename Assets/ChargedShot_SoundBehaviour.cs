using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class ChargedShot_SoundBehaviour : StateMachineBehaviour
{
    AudioSource audioSource;
    public AudioClip audioSound;
    public bool loop = false;

    //OnStateEnter is called before OnStateEnter is called on any state inside this state machine
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
            audioSource = animator.transform.GetComponent<AudioSource>();
            audioSource.clip = audioSound;
            audioSource.loop = loop;
       
    }

    // OnStateUpdate is called before OnStateUpdate is called on any state inside this state machine
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        if ((animator.GetLayerWeight(1) >= 1) && (audioSource.isPlaying == false))
        {
            audioSource.clip = audioSound;
            audioSource.Play();
        }
    }

    // OnStateExit is called before OnStateExit is called on any state inside this state machine
    override public void OnStateExit(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        audioSource.Stop();
        audioSource.loop = false;
    }

